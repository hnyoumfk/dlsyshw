class MatMul(TensorOp):
    def compute(self, a, b):
        ### BEGIN YOUR SOLUTION
        return array_api.matmul(a, b)
        ### END YOUR SOLUTION

    def gradient(self, out_grad, node):
        ### BEGIN YOUR SOLUTION
        lhs, rhs = node.inputs
        lrank = len( lhs.shape )
        rrank = len( rhs.shape )

        gl = matmul(out_grad, transpose(rhs))
        for _ in range(rrank - lrank):
            gl = summation(gl, (0))

        gr = matmul(transpose(lhs), out_grad )
        for _ in range(lrank - rrank):
            gr = summation(gr, (0))
        return gl , gr
        ### END YOUR SOLUTION


def matmul(a, b):
    return MatMul()(a, b)


