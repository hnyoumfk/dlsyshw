class Transpose(TensorOp):
    def __init__(self, axes: Optional[tuple] = None):
        self.axes = axes if axes is not None else (-1,-2)

    def compute(self, a):
        ### BEGIN YOUR SOLUTION
        axis_list = list(range(len(array_api.shape(a))))
        axis_list[self.axes[1]] , axis_list[self.axes[0]] = axis_list[self.axes[0]] , axis_list[self.axes[1]]
        return array_api.transpose(a, axes=axis_list)
        ### END YOUR SOLUTION

    def gradient(self, out_grad, node):
        ### BEGIN YOUR SOLUTION
        return transpose(out_grad, self.axes)
        ### END YOUR SOLUTION


def transpose(a, axes=None):
    return Transpose(axes)(a)


