        ### END YOUR SOLUTION

    def gradient(self, out_grad, node):
        ### BEGIN YOUR SOLUTION
        a = node.inputs[0]
        new_shape = list(a.shape)
        if self.axes is None:
            self.axes = list(range(len(a.shape)))
        for i in self.axes:
            new_shape[i] = 1
        g =  broadcast_to(reshape(out_grad, tuple(new_shape)), a.shape)
        return g
        ### END YOUR SOLUTION


